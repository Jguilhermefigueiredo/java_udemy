public class OperadoresComparacao {
    /*
     * igual a (==)
     * diferente de (!=)
     * maior que (>)
     * menor que (<)
     * maior ou igual (>=)
     * menor ou igual (<=)
     */
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        System.out.println("a = 10 e b = 20");
        System.out.print("a == b = "); System.out.println(a == b);
        System.out.print("a != b = "); System.out.println(a != b);
        System.out.print("a > b = "); System.out.println(a > b);
        System.out.print("a < b = "); System.out.println(a < b);
        System.out.print("a >= b = "); System.out.println(a >= b);
        System.out.print("a <= b = "); System.out.println(a <= b);
    }

}
