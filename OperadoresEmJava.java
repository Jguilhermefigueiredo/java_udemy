public class OperadoresEmJava {

    //operadores aritiméticos: adição(+), subtração(-), multiplicação(*), divisão(/) e resto da divisão(%).
    public static void main(String[] args) {
        int a = 10;
        int b = 2;

        System.out.print("a + b = "); System.out.println(a + b);
        System.out.print("a - b = "); System.out.println(a - b);
        System.out.print("a * b = "); System.out.println(a * b);
        System.out.print("a / b = "); System.out.println(a / b);
        System.out.print("a % b = "); System.out.println(a % b);
    }
}
