public class OperadoresLogicos {
    /*
     * E lógico (&&)
     * OU lógico (||)
     * NÃO lógico (!)
     */
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        boolean c = true;
    
        System.out.print("a < b && c = "); System.out.println(a < b && c);
        System.out.print("a > b || c = "); System.out.println(a > b || c);
        System.out.print("!c = "); System.out.println(!c);
        
    }

}
